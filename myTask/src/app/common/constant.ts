export const REGEX = {
    NAME:/^[A-Za-z\s]{1,}[\.]{0,1}[A-Za-z\s]{0,}$/,
    PHONE_NUMBER:/^[1-9]{1}[0-9]{9}$/,
    EMAIL:/^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))/,
    PASSWORD:/^(?=.*[A-Za-z])(?=(.*[\d]){1,})(?=.*?[^\w\s]).{8,}$/,
    ADDRESS:/^[a-zA-Z0-9\s,.'-]{3,}$/
}